// Здесь нет I/O — ни сети, ни БД, ни времени.
package domain

// Color обозначает цвет шашек.
type Color uint8

const (
	// White — белые. Голова на пункте 24, дом на пунктах 1–6.
	White Color = iota
	// Black — чёрные. Голова на пункте 12, дом на пунктах 13–18.
	Black
)

// Board — расстановка шашек по 24 пунктам.
//
// Индекс i соответствует пункту i+1 (т.е. b[0] — пункт 1, b[23] — пункт 24).
// Кодировка значения:
//   - > 0 — белые шашки, количество равно значению;
//   - < 0 — чёрные шашки, количество равно модулю значения;
//   - == 0 — пункт пуст.
//
// На одном пункте могут стоять шашки только одного цвета (инвариант,
// проверяемый правилами хода).
type Board [24]int8

// InitialBoard возвращает доску в стартовой расстановке длинных нард:
//   - 15 белых шашек на пункте 24 (b[23] == 15);
//   - 15 чёрных шашек на пункте 12 (b[11] == -15);
//   - остальные пункты пусты.
//
// TDD plan #1.
func InitialBoard() Board {
	var b Board
	b[23] = 15  // 15 белых на пункте 24 (голова белых)
	b[11] = -15 // 15 чёрных на пункте 12 (голова чёрных)
	return b
}

