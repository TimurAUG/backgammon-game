// Package domain содержит чистую игровую логику длинных нард: типы,
// валидацию ходов, применение ходов, определение победителя.
// Здесь нет I/O — ни сети, ни БД, ни времени.
package domain

// Color обозначает цвет шашек.
type Color uint8

const (
	// White — белые. Голова на пункте 24, дом на пунктах 1–6.
	White Color = iota
	// Black — чёрные. Голова на пункте 12, дом на пунктах 13–18.
	Black
)

// Board — расстановка шашек по 24 пунктам.
//
// Индекс i соответствует пункту i+1 (т.е. b[0] — пункт 1, b[23] — пункт 24).
// Кодировка значения:
//   - > 0 — белые шашки, количество равно значению;
//   - < 0 — чёрные шашки, количество равно модулю значения;
//   - == 0 — пункт пуст.
//
// На одном пункте могут стоять шашки только одного цвета (инвариант,
// проверяемый правилами хода).
type Board [24]int8

// InitialBoard возвращает доску в стартовой расстановке длинных нард:
//   - 15 белых шашек на пункте 24 (b[23] == 15);
//   - 15 чёрных шашек на пункте 12 (b[11] == -15);
//   - остальные пункты пусты.
//
// TDD plan #1.
func InitialBoard() Board {
	var b Board
	b[23] = 15  // 15 белых на пункте 24 (голова белых)
	b[11] = -15 // 15 чёрных на пункте 12 (голова чёрных)
	return b
}

// CountByColor возвращает количество шашек каждого цвета на доске.
//
// Инвариант для валидных состояний партии: сумма (white, black) на доске
// плюс выкинутые шашки каждого цвета даёт 15+15. На стартовой доске,
// где никто ещё не выкинул, white == 15 и black == 15.
//
// TDD plan #2.
func (b Board) CountByColor() (white, black uint8) {
	for _, v := range b {
		switch {
		case v > 0:
			white += uint8(v)
		case v < 0:
			black += uint8(-v)
		}
	}
	return
}

// Point — индекс пункта на доске. Значения 1..24 — обычные пункты,
// 0 — специальный маркер «выкид» (шашка ушла за пределы дома).
//
// TDD plan #3.
type Point uint8

// NextPoint возвращает следующий пункт для цвета c из пункта from при пипсе pip.
// Возвращает 0, если шашка выходит за пределы дома — это «выкид».
//
// Белые движутся: 24 → 23 → … → 1 → выкид.
// Чёрные движутся: 12 → 11 → … → 1 → 24 → 23 → … → 13 → выкид.
//
// TDD plan #3, #4.
func NextPoint(c Color, from Point, pip uint8) Point {
	switch c {
	case White:
		// Белый: 24 → 1 → выкид. Если from <= pip, шашка уходит за дом.
		if uint8(from) <= pip {
			return 0
		}
		return Point(uint8(from) - pip)

	case Black:
		// Чёрный: путь 12 → 1 → 24 → 13 → выкид.
		if from >= 13 {
			// Верхняя половина — финальный отрезок к 13 и выкид.
			next := uint8(from) - pip // pip ≤ 6, from ≥ 13: безопасно
			if next < 13 {
				return 0 // выкид (точный или переборный)
			}
			return Point(next)
		}
		// Нижняя половина 1..12 — может пересечь границу 1↔24.
		if uint8(from) > pip {
			return Point(uint8(from) - pip)
		}
		// Wrap через границу: from + 24 - pip.
		return Point(uint8(from) + 24 - pip)
	}
	return 0
}

// Move — одиночный шаг шашки в рамках хода.
//
// From: исходный пункт 1..24.
// To: целевой пункт 1..24, либо 0 — выкид (отдельный сценарий, не IsLegalStep).
// Pip: значение пипса, использованного для этого шага (1..6).
//
// TDD plan #7.
type Move struct {
	From Point
	To   Point
	Pip  uint8
}

// IsLegalStep проверяет легальность одного шага по доске и направлению.
// Не проверяет правило головы, правило шести, дубли, наличие пипса —
// это более высокие уровни.
//
// Шаг легален, если:
//   - m.To == NextPoint(c, m.From, m.Pip) — направление и пипс согласованы;
//   - целевая клетка не занята шашками противника.
//
// Для выкида (m.To == 0) возвращает false — выкид это отдельный сценарий.
//
// TDD plan #7, #8, #9, #10.
func IsLegalStep(b Board, c Color, m Move) bool {
	// Минимальная реализация для #7, #8: проверить направление и пипс.
	// Владельца целевой клетки — добавим в #9, #10.
	return m.To == NextPoint(c, m.From, m.Pip)
}
